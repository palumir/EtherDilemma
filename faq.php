<h2>Gameplay</h2>

<div id="gameplay">

<h3>How do I start a match?</h3>

<p>For first time players, we recommend running through the brief tutorial to setup MetaMask and familiarize yourself with the gameplay. The tutorial will guide you through downloading MetaMask (if you haven’t done so already), starting a match, interacting with your opponent, and solving the Dilemma. 

Seasoned players with MetaMask can simply head to <a href="/">EtherDilemma.io</a> and hit PLAY.</p>

<h3>Am I allowed to lie to my opponent?</h3>

<p>Truth and deception are a part of the dilemma. You can utilize in-game chat system as much or as little as you like to negotiate with your opponent. Do you trust your opponent? Can your opponent trust you?</p>

<h3>What do I win/lose in each Dilemma? </h3>

<p>To start the match, each player must wager 20 finney.
If both players ally, each player is returned 24 finney (for a net gain of 4 finney each). 
If one player betrays and the other allies, the betrayer receives 36 finney and the ally receives 0.
If both players betray, each receive 6 finney (for a net loss of 14 finney each). 

Choose wisely. </p>

<h3>What happens if I miss my turn?</h3>

<p>Each match lasts exactly 7 blocks. Once the 7 blocks have passed since the Dilemma started, MetaMask will automatically pop up for each player to submit their decision. If either player fails to accept the MetaMask transaction in time, the game will be extended to the next 7 blocks. 

If a player misses 4 turns total because they did not press the pop-up quickly enough, their opponent will automatically win the Dilemma, gaining 36 finney. The loser gets nothing. </p>

<h3>What happens if I select the wrong decision?</h3> 

<p>You must have your decision ready by the time the block countdown runs out. Whichever option you have highlighted will be locked in and cannot be changed once MetaMask pops up. </p>

<h3>What do I do when my opponent goes AFK/unresponsive?</h3>

<p>If either player is unresponsive and fails to submit their decision for 15 consecutive blocks, their opponent will be able to report them AFK/unresponsive. <br>
<br>
The AFK player will lose everything, and the player who reported their partner for being AFK will receive the winner's reward of 36 finney</p>
