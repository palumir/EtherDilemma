<h2>Cryptocurrency</h2>

<div id="cryptocurrency">
<h3>What is Ethereum and ETH?</h3>

<p>Ethereum is an open-source platform capable of running smart contracts - AKA decentralized apps (called dApps). The cryptocurrency Ether (ETH) itself powers the network, thereby allowing the applications to run. </p>

<h3>How do I purchase ETH?</h3>

<p>ETH can be purchased directly on MetaMask if you live within the United States. Simply click the MetaMask extension on your browser, make sure the ‘Main Ethereum Network’ is selected, and click ‘Buy’. You’ll then be directed to Coinbase to complete the purchase. 

For international users, you’ll need to purchase ETH directly on a digital exchange and follow their instructions for sending it to your MetaMask account. Your MetaMask account address can be found by clicking the three dots to the right of your account name (name is ‘Account 1’ in the example below). </p>

<h3>Is EtherDilemma an ICO? Is there a coin or currency?</h3> 

<p>No to all! EtherDilemma is simply a game built on the Ethereum blockchain. There is no ICO or ‘coin’ associated with EtherDilemma. </p>

<h3>What is MetaMask?</h3>

<p>MetaMask is a digital wallet extension that allows you to run dApps (apps on the Ethereum blockchain... AKA EtherDilemma) securely on your browser. To learn more on MetaMask and how to install, head to the <a href='http://metamask.io'>MetaMask site</a>.</p>

<h3>How do I install Metamask?</h3>

<p>MetaMask can be downloaded and used on several browsers (we recommend Google Chrome) by heading to <a href='http://metamask.io'>MetaMask.io</a>. Be sure to write down your seed words (used to reset your password if lost or forgotten) and password so you are not locked out of your account. </p>

<h3>Re-installing - save your seed words</h3>

<p>If you encounter an issue with MetaMask and the extension is no longer working, you can easily uninstall/delete the extension, re-download MetaMask, and setup your account using the same seed words. </p>

<h3>How do I add my ETH to Metamask?</h3>

<p>MetaMask allows you to purchase ETH directly within the extension by pressing the 'Buy' button if you live within the United States. International users will need to purchase ETH via an external exchange then transfer their ETH to MetaMask. <a href='https://blog.ujomusic.com/how-to-buy-eth-using-metamask-coinbase-ecffe9ede78e'>Click here for details on how to do this</a>. </p>

<h3>Are EtherDilemma and MetaMask secure?</h3>

<p>Yes, of course! The EtherDilemma web-site itself is simply an interface for the smart contract which is currently running on the Ethereum network. In fact, the site is programmed almost entirely in JavaScript, so the code actually runs on your computer, which in turn communicates through MetaMask to the contract on the Ethereum blockchain. So, EtherDilemma is fundamentally as secure as the Ethereum network that the smart contract that is running on, since it handles all of the back-end work for the game.<br><br>
As for MetaMask, it is one of the most popular Ethereum wallets used today -- your funds are password protected and only accessible via your browser on your computer. However, since it is always possible that a computer becomes comprimised by an attacker, whether physically or remotely, it is typically recommended that you store any significant amount of cryptocurrency in a hardware or paper wallet, since they are widely considered to be the most secure type of storage.</p>

<h3>What is ‘gas’?</h3>

<p>‘Gas’ is essentially the cost to power a single transaction on the Ethereum network. Unlike traditional games and apps that are stored on a private server, all dApps on the blockchain are decentralized and powered by the shared computational power of all miners on the network. For this reason, all interactions with the blockchain require ‘gas’, or a small fee to complete the process.

<a href='https://www.cryptocompare.com/coins/guides/what-is-the-gas-in-ethereum/'>Read more about gas and the standard transaction costs here</a>. </p>
