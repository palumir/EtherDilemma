<div id="general">
<h3>What is EtherDilemma?</h3>

<p>EtherDilemma is a high stakes, head-to-head game of trust and deception. Players face the seemingly simple dilemma: ally your opponent and split the payout… or betray to win (and risk) it all.</p>

<h3>What makes EtherDilemma different from other games?</h3>

<p>EtherDilemma is one of only a handful of games built on the decentralized Ethereum blockchain (called dApps), and the first to create a live, player-versus-player wagering system.</p>

<h3>What is the prisoner’s dilemma?</h3>

<p>The prisoner’s dilemma is a paradox in which two individuals (in this case, complete strangers) decide between cooperating to receive a lesser, mutually beneficial reward, or opt to betray their opponent to claim the full reward for themselves. The reward is severely reduced for both individuals shall they both betray. Choose wisely.</p>

<h3>What do I need to play EtherDilemma?</h3>

<p>To play EtherDilemma, you’ll need:

A computer (PC or laptop) with a browser capable of supporting MetaMask - we recommend Google Chrome or FireFox. 
MetaMask, a digital wallet extension that allows you to run dApps (apps on the ethereum blockchain... aka EtherDilemma) securely on your browser.
Ether on your MetaMask account - learn how to purchase ETH here. </p>

<h3>Who is my opponent? Will I be able to recognize my opponent?</h3> 

<p>Your opponent is a player found via randomized matchmaking. Players can choose a custom ‘username’ in each match (and change it at their discretion), thereby preserving the anonymity of EtherDilemma.</p>

<h3>Does it cost money to play?</h3>

<p>Potentially - every match requires both players to wager 20 finney (what is a finney? Read more below.) on the outcome of the Dilemma. Depending on each players’ choice to ally or betray, the payout ranges between 0 and 36 finney per player per match (see the full Payout breakdown here).</p>

<h3>What is a ‘finney’?</h3>

<p>A finney is one one-thousandths of an ether (ETH), meaning the value of one finney fluctuates with the value of ether. For example, if one ETH is worth $500, a finney is 1/1000 of $500, or 50 cents.</p>

<h3>Can I use USD or another currency instead of ETH?</h3> 

<p>No - the only way to play EtherDilemma is to own ether on a digital wallet. However, you can purchase ETH with USD and other currencies - read more on How to Purchase ETH here (*Link to crypto section).</p>

<h3>Can I play without wagering finney?</h3> 

<p>Nope - EtherDilemma is a high-stakes game of trust and deception. Both players need to have a little skin in the game.</p>
